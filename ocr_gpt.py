        raise RuntimeError(f"–û–∂–∏–¥–∞–ª—Å—è JSON-–º–∞—Å—Å–∏–≤ –ø–æ—Å–ª–µ –ø—Ä–∞–≤–∫–∏, –∞ –ø—Ä–∏—à–ª–æ:\n{text}")

